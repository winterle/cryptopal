def hamming_dist(str1,str2)

  bytes1 = []
    str1.to_s.each_byte do |char|
      bytes1.push(char.to_i)
    end

  h_dist = 0
  i = 0
  #compare characters
    str2.to_s.each_byte do |char|
      cmp = char.to_i^bytes1[i].to_i
      (0..7).each do #one byte
        if cmp%2==1 #check for LSB set (bits differ)
          h_dist +=1
        end
        cmp = cmp>>1 #shift LSB out
      end
      i+=1
    end
  return h_dist

end


def try_keysizes(min_len,max_len)
  keylen = 0.0+min_len
  h = 0
  h_min = 32456344.0
  best_len = 0
  (min_len..max_len).each do |size|
    #take 1st keylen bytes from cipher and calculate hamming dist to 2nd keylen bytes, then normalize
    h = 0
    (0...$cipher.size/size-1).each do |block|
      h+=hamming_dist($cipher[block*keylen,keylen],$cipher[(block+1)*(keylen),keylen])
    end
    h = h.to_f/(keylen.to_f*($cipher.length / keylen.to_f))
    keylen+=1
    if h < h_min
      h_min = h
      best_len = size
    end
  end
  puts"best hamming distance (#{h_min}) found, keylen = #{best_len}"
  return best_len
end

def get_block_from_file(f, size)
  ret = []
  i = 0
  until i >=f.length do
    ret.push(f[i])
    i += size
  end
  return ret.join
end

def xor_thekey(enc_string, i)
  #Then xor's that string by a single character
  ret = []

  enc_string.each_byte do |b|
    ret.push((b ^ i).chr)
  end
  return  ret.join
end

